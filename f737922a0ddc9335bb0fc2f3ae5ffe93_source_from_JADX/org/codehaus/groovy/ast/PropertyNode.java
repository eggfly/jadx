package org.codehaus.groovy.ast;

import groovyjarjarasm.asm.Opcodes;

public class PropertyNode extends AnnotatedNode implements Opcodes, Variable {
    private FieldNode DW;

    public String t_() {
        return this.DW.t_();
    }

    public ClassNode s_() {
        return this.DW.s_();
    }

    public boolean u_() {
        return this.DW.u_();
    }
}
